	function Page_Main() {
		global $objForm, $Language, $gsFormError;

		// Load key from QueryString
		if (@$_GET["nu_impactoRisco"] <> "") {
			$this->nu_impactoRisco->setQueryStringValue($_GET["nu_impactoRisco"]);
