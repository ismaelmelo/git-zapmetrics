	function Page_Main() {
		global $objForm, $Language, $gsFormError;

		// Load key from QueryString
		if (@$_GET["nu_usuarioSuporte"] <> "") {
			$this->nu_usuarioSuporte->setQueryStringValue($_GET["nu_usuarioSuporte"]);
